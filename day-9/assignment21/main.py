# if else
# checks one string contains in another String

while True:
    user_input = input("Select the options(add, update, show):")
    
    if "add" in user_input: 
        print("inside add option")
        
    elif "update" in user_input:
        print("inside update option")
        
    elif "show" in user_input:
        print("inside show option")